  sku: string;
  categorySlug: string;
  brand: string;
  shortDescription: string;
  wattage: string;
  lifespan: string;
  colorTemperature: string;
  baseType: string;
  compatibility: string[];
  specs: Record<string, string>;
  /**
   * Készlet státusz — a felhasználói felület a szállítási ígéretet ennek alapján mutatja.
   * - in_stock: raktáron, akár 2 munkanap alatt kézbesítve.
   * - lead_time: jellemző átfutású tétel, leadTimeBusinessDays megadása ajánlott.
   * - on_request: egyedi visszaigazolás szükséges (ritka típus).
   * Alapértelmezés (mező hiányában): "lead_time", ~9 munkanap.
   */
