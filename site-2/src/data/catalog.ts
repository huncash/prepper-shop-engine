export function getCategory(slug: string) {
  return categories.find((c) => c.slug === slug);
}

export function getProduct(slug: string) {
  return products.find((p) => p.slug === slug);
}

export function productsByCategory(slug: string) {
  return products.filter((p) => p.categorySlug === slug);
}

export type ResolvedStock = {
  status: "in_stock" | "lead_time" | "on_request";
  leadTimeBusinessDays?: number;
  checkedAt?: string;
};

/**
 * A termékhez tartozó hatályos készletadatot adja vissza.
 * Egyetlen forrásként használjuk a UI-ban; később az automatizált
 * készletfrissítés override-jai is itt fűződhetnek hozzá.
 */
export function getProductStock(product: Product): ResolvedStock {
  const status = product.stockStatus ?? "lead_time";
  const leadTimeBusinessDays =
    status === "lead_time" ? product.leadTimeBusinessDays ?? 7 : undefined;
  return { status, leadTimeBusinessDays, checkedAt: product.stockCheckedAt };
}
